package JavaPrograms.AFundamentalOfJava;

public class Loops8DoWhile {
    public static void main(String[] args) {
        int i = 1;
        do{
            System.out.println("Dada");
            i++;
        }
        while(i<0);
    }
}
//the 'do while' loop will execute the loop  at least one time, before checking if condition is true.