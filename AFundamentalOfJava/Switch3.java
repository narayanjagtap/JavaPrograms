package JavaPrograms.AFundamentalOfJava;

public class Switch3 {
    public static void main(String []args){
        int num=200;

        switch(num)
        {
            case 200: System.out.println("case1");
            break;

            case 300: System.out.println("case2");
            break;

            case 100: System.out.println("case3");
            break;

        }
    }
}
