package JavaPrograms.DArray;

public class Array3 {
    public static void main(String[] args) {
        int data[] = new int[4];
        data[0] = 10;
        data[1] = 20;
        data[3] = 40;
        data[2] = 30;

        for(int i=0; i<4; i++){
            System.out.println(data[i]);
        }
    }
}
